            <p class="lead"> The Bureau of Customs (abbreviated BoC or BOC; Filipino: Kawanihan ng Adwana) is a Philippine government agency under the Department of Finance. The Bureau of Customs was established on February 6, 1902 by the Insular Government of the Philippine Islands of the United States of America, during the American Colonial Era of the Philippines. </p>

     <br>  
   <h3 style="font-weight:bolder;" >Functions:</h3>
   <p class="lead bullet" style="text-align:justify;"> <i class="fas fa-circle pr-2" style="font-size:14px;"></i> Assessment and collection of customs revenues from imported goods and other dues, fees, charges, fines and penalties accruing under the CMTA;<br>
                           <i class="fas fa-circle pr-2" style="font-size:14px;"></i>  Supervision and control over the handling of foreign mails arriving in the Philippines for the purpose of collecting revenues and preventing the entry of contraband;<br>
                           <i class="fas fa-circle pr-2" style="font-size:14px;"></i> Simplification and harmonization of customs procedures to facilitate movement of goods in international trade<br>
                           <i class="fas fa-circle pr-2" style="font-size:14px;"></i> Border control to prevent entry of smuggled goods;<br>
                           <i class="fas fa-circle pr-2" style="font-size:14px;"></i> Prevention and suppression of smuggling and other customs fraud;<br>
                           <i class="fas fa-circle pr-2" style="font-size:14px;"></i> Facilitation and security of international trade and commerce through an informed compliance program;<br>
                           <i class="fas fa-circle pr-2" style="font-size:14px;"></i> Supervision and control over the entrance and clearance of vessels and aircraft engaged in foreign commerce; </p>
                           
